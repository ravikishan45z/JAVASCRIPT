console.log(2 < 3);
console.log(2 == 2);
console.log(2 != 1);

console.log(NaN == NaN);
console.log(NaN == 0);
console.log(NaN >= NaN);
console.log(NaN >= NaN);
console.log(NaN != NaN);

console.log(undefined == undefined);
console.log(undefined === undefined);
console.log(undefined != undefined);
console.log(undefined >= undefined);
